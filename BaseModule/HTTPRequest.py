def getHeader():
    header = {'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
              'Accept-Encoding': 'gzip, deflate, br',
              'Accept-Language': 'zh-CN,zh;q=0.8,zh-TW;q=0.7,zh-HK;q=0.5,en-US;q=0.3,en;q=0.2',
              'Connection': 'keep-alive',
              'User-Agent': user_agent()
              }

    return header


def get(url, parms={}, headers=getHeader(), timeout=30, retry_num=3, retry_interval=5, *args, **kwargs):
    '''

    :param url: get请求的 url
    :param header: 请求头，默认为 header() 方法自动获取
    :param timeout: timeout 默认为 30 秒
    :param retry_num: 重新尝试连接的次数，默认为3次
    :param retry_interval: 重连前暂时的时间，单位为秒
    :return: class:`Response <Response>` object
            if request have error return 'None'
    :param args:
    :param kwargs:
    :return:
    '''
    while True:
        try:
            response = requests.get(url=url, params=parms, headers=headers, timeout=timeout)
            return response
        except Exception as error:
            logger.debug('Httprequest get error:{}\n{}'.format(url, error))
            retry_num -= 1
            if retry_num < 0 :
                return None
            time.sleep(retry_interval)


def post(url, data, headers=getHeader(), timeout=30, retry_num=3, retry_interval=5, *args, **kwargs):
    '''

    :param url: get请求的 url
    :param data:
    :param header: 请求头，默认为 header() 方法自动获取
    :param timeout: timeout 默认为 30 秒
    :param retry_num: 重新尝试连接的次数，默认为3次
    :param retry_interval: 重连前暂时的时间，单位为秒
    :return: class:`Response <Response>` object
            if request have error return 'None'
    '''
    while True:
        try:
            response = requests.request("POST", url, data=data, headers=headers, timeout=timeout)
            return response
        except Exception as error:
            logger.debug('Httprequest Post error:{}\n{}'.format(url, error))
            retry_num -= 1
            if retry_num < 0:
                return None
            time.sleep(retry_interval)


